def translate_file(input_path: str, output_path: str, translations: dict) -> None:
    """Translate Italian JSON to Hindi with validation."""

    # Read Italian file
    with open(input_path, 'r', encoding='utf-8') as f:
        data = json.load(f)

    # Translate all keys
    hindi_data = {}
    for key, it_value in data.items():
        if key in translations:
            hindi_data[key] = translations[key]
        else:
            # Fallback: use Italian if translation not found
            print(f"WARNING: No translation for key: {key}")
            hindi_data[key] = it_value

    # Validate HTML preservation in data-i18n-html keys
    for key, value in hindi_data.items():
        if 'data-i18n-html' in key or '<' in str(value):
            # Check that HTML tags are preserved
            if '<strong>' in str(data.get(key, '')) and '<strong>' not in str(value):
                print(f"WARNING: HTML tag lost in {key}")

    # Write Hindi file
    with open(output_path, 'w', encoding='utf-8') as f:
        json.dump(hindi_data, f, ensure_ascii=False, indent=2)

    print(f"✓ Translation complete: {output_path}")
    print(f"  Keys translated: {len(hindi_data)}/{len(data)}")

